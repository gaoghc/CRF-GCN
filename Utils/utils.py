class Logger(object):
    def __init__(self, output_file_path):
        self.terminal = sys.stdout
        self.log = open(output_file_path, "w+")

    def write(self, message):
        self.terminal.write(message)
        self.log.write(message)

    def flush(self):
        # this flush method is needed for python 3 compatibility.
        # this handles the flush command by doing nothing.
        # you might want to specify some extra behavior here.
        pass

def logging(file_name, model_name, residual, seed, output_path, verbose=0):
    output_file_name = file_name.split('.')[0] + '_' + model_name + '_' + residual + '_' + seed + '_' + time.strftime("%d-%m-%Y_") + time.strftime("%H-%M-%S_") + "log.txt"
    output_file_path = os.path.join(output_path, output_file_name)
    sys.stdout = Logger(output_file_path)
    if verbose > 1:
        print(sys.argv)

    return output_file_path


